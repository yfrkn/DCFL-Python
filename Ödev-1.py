#Küpün Hacmini ve Yüzey Alanını Bulan Program
kenar = float(input("Küpün kenar uzunluğunu giriniz : "))
hacim = kenar**3
yuzeyalani = (kenar**2)*6
print("Küpün Hacmi = ", hacim)
print("Küpün Yüzey Alanı = ", yuzeyalani)

#Dairenin Çevresini ve Alanını Bulan Program
r = int(input("Dairenin yarı çap uzunluğunu giriniz : "))
alan = 3.14*(r**2)
cevre = 2*3.14*r
print("Dairenin Çevresi = ", cevre)
print("Dairenin Alanı = ", alan)
